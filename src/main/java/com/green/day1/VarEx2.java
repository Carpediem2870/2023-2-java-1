package com.green.day1;

public class VarEx2 {
    public static void main(String[] args) {  // args < 매개변수 parameter // 파란색은 예약어(정해져있는 규칙)
        int x = 10, y = 20;  // x가 20을갖고싶어하고 y가 10을 갖고싶어함
        int c = x;



        x=y;
        y=c;


        System.out.println("x:" + x + " y:" + y);


        System.out.println("x : " + x);
        System.out.println("y : " + y);

        }
    }

