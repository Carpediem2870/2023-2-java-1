package com.green.day5.ch4;

public class FlowEx16 {
    public static void main(String[] args) {

        // 중첩 반복문을 사용하여
        // 가로 10개, 세로 5줄
        // 별을 찍으시오



        char sss = '*';
        String str = String.valueOf(sss);


        for (int i = 0; i < 5; i++) {

            System.out.printf("%s\n",str);
        }System.out.println();
        }
    }
